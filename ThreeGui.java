    private void equalSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalSignActionPerformed
        switch(operation){
            case "addition":
                result = numTwo + numOne;
                TF.setText(Long.toString((long) result));
                break;
            case "subtraction":
                result = numTwo - numOne;
                TF.setText(Long.toString((long) result));
                break;    
            case "multiplication":
                result = numTwo * numOne;
                TF.setText(Long.toString((long) result));
                break;
            case "division":
                result = numTwo / numOne;
                TF.setText(String.format("%.2f",result));
                break;
            case "modulo":
                result = numTwo % numOne;
                TF.setText(Long.toString((long) result));
                break;
        }
    }//GEN-LAST:event_equalSignActionPerformed

